package pl.mpas.advanced_programming.P2_2;

public class Car {

    String brand, colour, model;
    int year;
    double capacity;


    public Car(String brand, String colour, String model, int year, double capacity) {
        this.brand = brand;
        this.colour = colour;
        this.model = model;
        this.year = year;
        this.capacity = capacity;
    }
}
